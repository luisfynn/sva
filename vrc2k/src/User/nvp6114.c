		I2C1_ByteWrite(I2C_NVP6114_ADDR,  0x49,0x00);
		MDINDLY_mSec(30);  
		I2C1_ByteWrite(I2C_NVP6114_ADDR,  0x48,0x40);
		MDINDLY_mSec(30);  
		I2C1_ByteWrite(I2C_NVP6114_ADDR,  0x48,0x60);	

		I2C1_ByteWrite(I2C_NVP6114_ADDR, 0xFF, BANK2);
